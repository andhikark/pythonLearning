name = "Putu Andhika Restu Kurnia"

print(len(name)) #length of string
print(name.find("A")) #find letter
print(name.capitalize()) #capitalize only first letter
print(name.upper()) #uppercase the letter
print(name.lower()) #lowercase the letter
print(name.isdigit()) #check if it's number
print(name.isalpha()) #check if it's contain only alphabet
print(name.count("u")) #count specific letter
print(name.replace("a","i")) #replace the letter
print(name * 3) #print multiple string